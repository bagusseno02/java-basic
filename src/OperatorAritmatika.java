public class OperatorAritmatika {

    public static void main(String[] args) {

        int a = 100;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        /* Augmanted Assignments */
        System.out.println(a += 10);
        System.out.println(a -= 10);
        System.out.println(a *= 10);
        System.out.println(a /= 10);
        System.out.println(a %= 10);

    }

}
