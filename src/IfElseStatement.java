public class IfElseStatement {

    public static void main(String[] args) {

        var nilai = 70;
        var absen = 90;

        if (nilai >= 70 && absen >= 90) {
            System.out.println("Selamat Anda Lulus");
        } else {
            System.out.println("Silahkan Coba Lagi Tahun Depan");
        }

    }

}
